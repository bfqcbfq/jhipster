package com.ivision.app.domain;


public class GeneralOcrLocation {

    private int top;
    private int left;
    private int width;
    private int height;
    public void setTop(int top) {
         this.top = top;
     }
     public int getTop() {
         return top;
     }

    public void setLeft(int left) {
         this.left = left;
     }
     public int getLeft() {
         return left;
     }

    public void setWidth(int width) {
         this.width = width;
     }
     public int getWidth() {
         return width;
     }

    public void setHeight(int height) {
         this.height = height;
     }
     public int getHeight() {
         return height;
     }

}